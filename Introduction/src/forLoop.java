
public class forLoop {

	public static void main(String[] args) {
      
		
		int i=1;
		while(i<=10){
			System.out.println(i);
			i=i+1;
		}
		System.out.println("**********");
		int j=10;
		while(j>=-10){
			System.out.println(j);
			j=j-1;
			}
        System.out.println("**********");
        int a;
        for(a=10;a>=-10;a--){
        	System.out.println(a);
        }
        
}
}